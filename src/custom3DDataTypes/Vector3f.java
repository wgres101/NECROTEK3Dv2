package custom3DDataTypes;

public class Vector3f {

	
	public float x;
	public float y; 
	public float z;
	
	//constructor
	
	public Vector3f(float x, float y, float z)
	{
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public Vector3f(int x, int y, int z)
	{
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
}
